export class StringUtils {
    static mapNoneEnglishToEnglish = (input: string) => {
        let output = "";
        for (let char of input) {
            output += CHARS_MAP[char] || char;
        }

        return output;
    };

    static checkContainNoneEnglishCharacter(input: string) {
        const regex = /[^a-zA-Z0-9\s.,?!'";:()&-]/;
        return regex.test(input);
    }
}
